#Operador AND
r = True and False
print(r)

#Operador OR
r = True or False
print(r)

#Operador NOT
r = not True
print(r)

#Operadores relacionales

#Operador ==
r = 5 == 3 
print(r)

#Operador !=
r = 5 != 3 
print(r)

#Operador <
r = 5 < 3 
print(r)

#Operador >
r = 5 > 3 
print(r)

#Operador <=
r = 5 <= 3 
print(r)

#Operador >=
r = 5 >= 3 
print(r)